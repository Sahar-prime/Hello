/*
Цикл – это управляющая структура, которая позволяет многократно выполнить определённую часть кода (многократно повторить выполнение определённой части кода). Цикл зацикливает выполнение определённой части кода.

Виды циклов:
С предусловием – while;
С постусловием – do...while;
Цикл на заданное количество итераций – for.

Итерация – это однократное выполнение тела цикла.

Тело цикла – это код, который нужно зациклить.
Цикл while
Синтаксис:

while (условие) {
    группа_операторов; // Тело цикла
}

Порядок выполнения:
Проверяется условие (Condition).
Если условие вернуло true, выполняется тело цикла.
Происходит возврат в начало, и снова проверяется условие.
Цикл продолжается, пока условие не вернёт false.
Если условие вернуло false, происходит выход за пределы цикла.
Цикл do...while
Синтаксис:

do {
    группа_операторов; // Тело цикла
} while (условие);

Порядок выполнения:
Выполняется тело цикла.
Проверяется условие (Condition).
Если условие вернуло true, происходит возврат в начало, и снова выполняется тело цикла.
Цикл продолжается, пока условие не вернёт false.
Если условие вернуло false, происходит выход за пределы цикла.

Основные различия между while и do...while:

Проверка условия:
В while условие проверяется перед выполнением тела цикла.
В do...while условие проверяется после выполнения тела цикла.

Гарантия выполнения:
В while тело цикла может не выполниться ни разу, если условие изначально ложно.
В do...while тело цикла выполняется хотя бы один раз, независимо от условия.

Поведение:
while – сначала проверяет условие (думает), потом выполняет тело цикла (делает).
do...while – сначала выполняет тело цикла (делает), потом проверяет условие (думает).
Эти циклы используются в зависимости от того, когда и как часто нужно выполнять блок кода.
*/